CREATE DATABASE PETSHOP;

USE PETSHOP;



CREATE TABLE CLIENTE (
IDCLIENTE INT PRIMARY KEY AUTO_INCREMENT,
NOME_CLIENTE VARCHAR(45),
IDADE_CLIENTE VARCHAR(45),
TELEFONE_CLIENTE VARCHAR(45),
CELULAR_CLIENTE VARCHAR(45),
ENDEREÇO_CLIENTE VARCHAR(45)) AUTO_INCREMENT = 101;

CREATE TABLE PET (
IDPET INT PRIMARY KEY AUTO_INCREMENT,
NOME_PET VARCHAR(45),
TIPO_ANIMAL VARCHAR(45),
FK_CLIENTE INT,
FOREIGN KEY(FK_CLIENTE) REFERENCES CLIENTE(IDCLIENTE));

DESC PET;


