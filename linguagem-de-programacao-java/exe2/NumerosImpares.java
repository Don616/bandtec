/*

Crie uma classe chamada “NúmerosImpares” que: 

    Usando “for”, exiba todos os números ímpares entre 0 e 90.

*/


public class NumerosImpares {
    
    public static void main(String[] args){

        for(int i = 0; i < 90; i++){

            if(i % 2 == 1){

                System.out.println(i);

            }

        }


    }

}
