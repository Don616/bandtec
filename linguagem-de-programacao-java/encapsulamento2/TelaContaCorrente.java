    private void btnsacarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsacarActionPerformed

        String valorDigitado = txtValor.getText();
        Double valorConvertido = Double.valueOf(valorDigitado);
        
        if(valorConvertido <= conta1.getSaldo()){
            
            Double valorFinal = conta1.getSaldo() - valorConvertido;
            conta1.setSaldo(conta1.getSaldo() - valorConvertido);
            String valorFinalConvertido = String.valueOf(valorFinal);
            lblValorSaldo.setText(valorFinalConvertido);
            
        }
        

        
    }//GEN-LAST:event_btnsacarActionPerformed

    private void btndepositarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndepositarActionPerformed
       
        String valorDigitado = txtValor.getText();
        Double valorConvertido = Double.valueOf(valorDigitado);
        
        conta1.setSaldo(conta1.getSaldo() + valorConvertido);
        String valorFinalConvertido = String.valueOf(conta1.getSaldo());
        lblValorSaldo.setText(valorFinalConvertido);
    }//GEN-LAST:event_btndepositarActionPerformed

